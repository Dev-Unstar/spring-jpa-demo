    @Test
    @DisplayName("Querydsl 조회테스트1")
    public void queryDslTest(){
        this.createItemList();
        JPAQueryFactory queryFactory = new JPAQueryFactory(em);
        QItem qItem = QItem.item;
        JPAQuery<Item> query = queryFactory.selectFrom(qItem)
                .where(qItem.itemSellStatus.eq(ItemSellStatus.SELL))
                .where(qItem.itemDetail.like("%"+"테스트 상품 상세 설명" + "%"))
                .orderBy(qItem.price.desc());

        List<Item> itemList = query.fetch();
        for(Item item : itemList){
            System.out.println(item);
        }
    }

    @Test
    @DisplayName("Querydsl 조회테스트2")
    public void queryDslTest2(){
        this.createItemList2();
        QItem item = QItem.item;
        String itemDetail = "테스트 상품 상세 설명";
        int price = 10003;
        String itemSellStat = "SELL";
        //조건 생성
        BooleanBuilder booleanBuilder = new BooleanBuilder();   //조건을 만들어주는 빌더역할?
        booleanBuilder.and(item.itemDetail.like("%"+itemDetail+"%"));
        booleanBuilder.and(item.price.gt(price));
        //batis의 dynamic쿼리처럼 사용하면되는것같음.
        if(StringUtils.equals(itemSellStat, ItemSellStatus.SELL)){
            booleanBuilder.and(item.itemSellStatus.eq(ItemSellStatus.SELL));
        }

        Pageable pageable = PageRequest.of(0,5);    //페이징 설정
        Page<Item> itemPagingResult = itemRepository.findAll(booleanBuilder, pageable); //쿼리수행
        List<Item> resultItemList = itemPagingResult.getContent();                      //결과추출
        //출력
        System.out.println("total elements : " + itemPagingResult.getTotalElements());
        for(Item resultItem : resultItemList){
            System.out.println(resultItem);
        }
    }
